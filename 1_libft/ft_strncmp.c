#include "libft.h"

int ft_strncmp(char *str1, char *str2, size_t n)
{
    size_t      i;
    while(str1[i] && str2[i] && n < i)
    {
        if(str1[i] != str2[i])
        {
            return(str1[i] - str2[i]);
            i++;
        }
    }
}
